function changeimage(a){
    var main = parent.frames['main'];
    if (a == "a1"){main.getElementById('img').src='1.png';}
    else if (a == "a2"){main.getElementById('img').src='2.png';}
    else if (a == "a3"){main.getElementById('img').src='3.png';}
    else if (a == "a4"){main.getElementById('img').src='4.png';}
    else if (a == "a5"){main.getElementById('img').src='5.png';}
    else if (a == "a6"){main.getElementById('img').src='6.png';}
}